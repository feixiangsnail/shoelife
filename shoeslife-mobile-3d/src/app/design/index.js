var modeDefaults = function(d, is) {
    var isGroup;
    d.map((item) => {
        isGroup = true;
        item.mode.map((mode, i) => {
            if (mode.isGroup) {
                mode.mode.map((group, j) => {
                    if (group.isMain && is && isGroup) {
                        group.isDefault = true;
                        isGroup = false;
                        return;
                    }
                    group.isDefault = false;
                })
                return;
            }
            if (i == 0 && is) {
                isGroup = false;
                mode.isDefault = true;
            } else mode.isDefault = false;
        })
    })
}
